package tubeclient

import "time"

// Config defines the configurations of Consumer
type Config struct {
	// Group is the name of a consumer group
	// Only one consumer in the same consumer Group can consume messages at any one time
	Group string
	// MasterAddress is the address of master server
	MasterAddress string
	// Topics are all topics that need to subscribe
	Topics []string
	// Consumer ID
	ConsumerID string
	// Timeout is the timeout for all requests
	Timeout time.Duration
	// MaxRegisterRetryTimes is the max register retry times
	MaxRegisterRetryTimes int
	// HeartBeatPeriodMs is the period of heartbeat
	HeartBeatPeriod time.Duration
}
